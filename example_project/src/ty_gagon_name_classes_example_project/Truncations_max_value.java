package ty_gagon_name_classes_example_project;

public class Truncations_max_value {

	public Truncations_max_value() {
		
		int age = 16;
		char letter = 't';
		double square_root = 2;
		boolean answer = true;
		
		System.out.println(age);
		
		System.out.println(letter);
		
		System.out.println(square_root);
		
		System.out.println(answer);
		
		age = age + 5;
		System.out.println(age);
		
		age += 3;
		System.out.println(age);
		
		age ++;
		System.out.println(age);
		
		
		
		int intResult = 3 / 2;
		double doubleResult1 = 3 / 2;
		double doubleResult2 = 3.0 / 2.0;
		float maxFloat = Float.MAX_VALUE;
		
		System.out.println(intResult);
		
		System.out.println(doubleResult1);
		
		System.out.println(doubleResult2);
		
		System.out.println(maxFloat);
	}

	public static void main(String[] args) {
	   new Truncations_max_value();

	}

}
